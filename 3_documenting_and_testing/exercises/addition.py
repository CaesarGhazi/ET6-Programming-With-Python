def addition(first_number,second_number):
    """Adds two numbers.

    Parameters:
    first_number,second_number, basically any number

    Returns:
    the two numbers added

    >>> addition(1,1)
    2

    >>> addition(2,1)
    3

    >>> addition(2,3)
    5
    """
    
    return first_number + second_number
